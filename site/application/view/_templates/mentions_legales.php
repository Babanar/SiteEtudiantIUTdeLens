                <h3>
                    <span id="titre_donnees_personnelles">Protection de vos données personnelles</span>
                </h3>
                <hr>
            </div>

            <div class="row">
                Conformément à la loi "Informatique et libertés" du 6 janvier 1978, aucune information personnelle n'y est collectée à l’insu des membres du réseau.
                <!--Le site du réseau professionnel a fait l’objet d’un enregistrement par les Correspondants Informatique et Libertés et porte le numéro d'autorisation 1339525.-->


                Le réseau professionnel n’est pas en libre accès. L’accès en est réservé aux membres du réseau, en ayant accepté la charte, et disposant d’un identifiant et d’un 
                mot de passe.

                Les informations recueillies auprès des membres du réseau par formulaire peuvent être consultées uniquement par les membres du réseau (étudiants, diplômés, 
                représentants d’entreprises et personnel de l'université dûment autorisés) s’étant tous engagés à respecter la charte de fonctionnement 
                du réseau à des fins d’insertion, de mobilité professionnelle ou de prise de contact avec les diplômés, futurs diplômés et personnel de l'université.


                Seule l’identité inscrite volontairelent par les usagers sera rendue public. En effet, le strict minimum des informations nécessaires à la 
                différenciation des usagers (Nom, prénom, date de naissance et informations relatives à la scolarité au sein de l'université) sont obligatoires. 
                Le renseignemeent de toute information relative aux données personelles (adresse, numéro de téléphone...) se fait à l'initiative de l'usager 
                et sera donc uniquement rendue publique si celui-ci en a fait le renseignement. 
                En aucun cas les données concernant les membres du réseau ne peuvent être exploitées à des fins commerciales ou politiques. 
                Chaque membre ou/et partenaires autorisés à consulter les données du réseau s'obligent à respecter cette disposition.


                Les membres du réseau disposent en ligne d'un droit d'accès, de modification et de suppression de ces données qui peut être exercé à tout moment 
                par le biais de la page <a href="#">contact</a>.
            </div>

            <div class="row">
                <h3>
                    <span id="titre_charte">Charte de l'Alumni de l'IUT de Lens</span>
                </h3>
                <hr>
            </div>


            <div class="row">
                Le réseau professionnel de l'Alumni de l'IUT de lens a pour but de faciliter l’insertion et la mobilité professionnelle 
                des diplômés ou futurs diplômés et en les mettant en relation entre eux et avec des entreprises partenaires, et de leur permettre de rester en relation avec 
                avec leurs camarades et professeurs.  

                <div class="row">
                    <div id="objet_chapitre">
                        <div class="col-md-offset-1">
                            <div class="row">
                                <h4>
                                    <span id="objet_chapitre_titre">Objet</span>
                                </h4>
                                <hr>
                            </div>

                            <div class="row">

                                La présente charte a pour but de fixer les conditions générales d'utilisation des services du réseau par ses membres.

                                Le réseau offre à ces derniers :


                                <ul id="servicesListe">
                                    <li>un annuaire des diplômés,</li>

                                    <li>un service d’offre d'emplois et de staqes,</li>

                                    <li>des services d'informations et d'actualité (newsletters, événements, agenda, etc...),</li>

                                    <li>un service de forums,</li>

                                    <li>La possibilité de contacter directement des anciens ou actuels camardes et professeurs</li>
                                </ul>

                                l'Alumni de l'IUT de Lens assure techniquement, directement ou indirectement, l'hébergement des services et la mise à disposition des outils. Chaque membre du réseau est responsable 
                                de l’utilisation qu’il en fait et des informations ou messages qu’il y diffuse.

                                Vous utilisez ce site sous votre seule et entière responsabilité. l'Alumni de l'IUT de Lens ne pourra être tenue pour responsable des dommages directs ou indirects, 
                                tels que, notamment, préjudice matériel, pertes de données ou de programmes, préjudice financier, résultant de l'utilisation de ce site ou de sites qui lui sont liés.

                                Nous attirons votre attention sur le fait que certains produits ou services présentés sur le site  peuvent faire l'objet de réglementations particulières 
                                voire de restrictions et/ou interdictions dans certains pays.

                                Vous devez, en conséquence, vous assurer que la loi du pays à partir duquel vous vous connectez autorise la consultation du présent site.

