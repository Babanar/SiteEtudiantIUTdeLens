                    <span id="titre_donnees_personnelles">Protection de vos données personnelles</span>
                </h3>
                <hr>
            </div>

            <div class="row">
                Conformément à la loi "Informatique et libertés" du 6 janvier 1978, aucune information personnelle n'y est collectée à l’insu des membres du réseau.
                <!--Le site du réseau professionnel a fait l’objet d’un enregistrement par les Correspondants Informatique et Libertés et porte le numéro d'autorisation 1339525.-->


                Le réseau professionnel n’est pas en libre accès. L’accès en est réservé aux membres du réseau, en ayant accepté la charte, et disposant d’un identifiant et d’un 
                mot de passe.

                Les informations recueillies auprès des membres du réseau par formulaire peuvent être consultées uniquement par les membres du réseau (étudiants, diplômés, 
                représentants d’entreprises et personnel de l'université dûment autorisés) s’étant tous engagés à respecter la charte de fonctionnement 
                du réseau à des fins d’insertion, de mobilité professionnelle ou de prise de contact avec les diplômés, futurs diplômés et personnel de l'université.


                Seule l’identité inscrite volontairelent par les usagers sera rendue public. En effet, le strict minimum des informations nécessaires à la 
                différenciation des usagers (Nom, prénom, date de naissance et informations relatives à la scolarité au sein de l'université) sont obligatoires. 
                Le renseignemeent de toute information relative aux données personelles (adresse, numéro de téléphone...) se fait à l'initiative de l'usager 
                et sera donc uniquement rendue publique si celui-ci en a fait le renseignement. 
                En aucun cas les données concernant les membres du réseau ne peuvent être exploitées à des fins commerciales ou politiques. 
                Chaque membre ou/et partenaires autorisés à consulter les données du réseau s'obligent à respecter cette disposition.


                Les membres du réseau disposent en ligne d'un droit d'accès, de modification et de suppression de ces données qui peut être exercé à tout moment 
                par le biais de la page <a href="#">contact</a>.
