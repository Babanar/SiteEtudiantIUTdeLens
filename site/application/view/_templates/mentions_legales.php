                            <div class="row">
                                <h4>
                                    <span id="acces_chapitre_titre">Conditions d'accés aux données du site</span>
                                </h4>
                                <hr>
                            </div>

                            <div class="row">

                                Toute personne faisant ou ayant fait partie, pour une durée quelconque, du personnel ou des étudiants de l'université d'Artois ainsi que les 
                                entreprises. Chaque membre devra auparavant s'être inscrit et avoir été validé par l'administration du site avant de pouvoir avoir accés
                                à l'ensemble des données qui y sont présentes.

                                Les membres du réseau disposent d’un identifiant et d’un mot de passe. Le mot de passe est personnel et confidentiel. Chaque membre inscrot est 
                                responsable de l'utilisation de son mot de passe et s’engage à ne pas le divulguer d’aucune façon.

                                Dès la première utilisation de l’accès, l’acceptation de la présente charte vaut comme contrat régissant les relations entre les administrateurs du site 
                                et le membre. Ce contrat est formé pour une durée d’un an, avec reconduction expresse par période d’un an par confirmation ou actualisation 
                                annuelle par le membre de ses informations individuelles. Le membre du réseau pourra mettre fin à ce contrat à tout moment.



