                        <div class="row">
                            <h4>
                                <span id="forum_mp_titre">Messages privés, Forums, circulation des messages </span>
                            </h4>
                            <hr>
                        </div>

                        <div class="row">

                            Les Forums de discussion et les messages privés sont réservés aux membres du réseau et personnes dûment habilitées. 
                            L'administration ne pourra être tenue pour responsable du contenu des messages distribués et transmis par 
                            les membres au travers de ces moyens de discussions. Les membres du réseau acceptent que, s'ils existent, 
                            les modérateurs des Forums exercent une surveillance sur les messages envoyés par les membres sur les Forums de discussion.

                            Par ailleurs, les membres s'engagent à ce qu ele contenu de ses messages ne viole en aucune façon :
                            <br><br>
                            <ul>
                                <li> les droits des personnes et des biens,</li>
                                <li>les lois et réglementations en vigueur en France, dans son pays de résidence ou dans le pays où ses données peuvent être envoyées,</li>
                                <li>et ne contienne ni virus, ni programmes susceptibles d’entraîner des dommages.</li>
                            </ul>

                            Il s'engage à ne pas transmettre de messages ou données illicites, diffamatoires, constituant des menaces ou attentatoires 
                            aux bonnes mœurs. La transmission de tout message non sollicité à caractère publicitaire ou marchand ou émis dans un but politique, 
                            syndical, religieux est interdite.
                            <br><br>
                            Le membre du réseau s'interdit l'envoi massif de courrier électronique aux autres membres du réseau. 
                            Il s'engage à respecter l'image et la réputation de l'IUT de Lens et de son Alumni et à ne pas se livrer à des déclarations ou des actions pouvant 
                            lui porter atteinte.
                            <br><br>
                            Le non respect de ces règles entraînera la radiation du réseau du membre auteur de message(s) litigieux et des poursuites de la part 
                            du propriétaire du site. 




