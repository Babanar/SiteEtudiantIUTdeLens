                        <div class="row">
                            En application de la loi n° 78-17 du 6 janvier 1978 relative à l'informatique, aux fichiers et aux libertés, 
                            chaque membre du réseau dispose des droits d'opposition, d'accès et de rectification des données le concernant. 
                            Tout membre du réseau peut librement modifier ou effacer les informations qu’il a lui-même saisies sur le réseau. 
                            Il peut par ailleurs exiger les rectifiées, complétées, clarifiées, mises à jour ou effacées les informations le 
                            concernant via son profil ou en passant par la page de contact pour exiger la modifications des informations dont il n'a pas accés 
                            par lui même.
                            <br><br>
                            Les informations contenues dans la base de données de l'Alumni de l'IUT de Lens restent la propriété des membres. 
                            l'administration est seule autorisée à les détenir et s'engage à ne pas les revendre ou les transmettre à des tiers, 
                            et à ce que ces informations ne soient pas traitées à d'autres fins que leur mise en ligne au sein du réseau. 
                            Toutefois, dans le cadre de partenariats avec des associations d’anciens constituées en groupe au sein du réseau, 
                            les données personnelles déposées par les membres des dites associations sur le réseau pourront leur être transmises avec 
                            l’accord des membres concernés.
                            <br><br>
                            l'Alumni de l'IUT de Lens est protégé par les dispositions du Code de la propriété intellectuelle, 
                            notamment sur la protection des bases de données. Toute personne qui se constituerait un fichier ou une base de données 
                            avec les informations présentes sur le réseau, et qui le mettrait à disposition d'un tiers, à titre onéreux 
                            ou gratuit, sans l'accord de l'Alumni et en violation de la présente charte, s'exposerait aux sanctions civiles et/ou 
                            pénales prévues par le Code de la propriété intellectuelle.
                            <br><br>
                            Le membre du réseau reconnaît être informé des risques liés à l’usage d'Internet et des réseaux, et notamment du fait que des 
                            informations relatives à des données personnelles le concernant peuvent être copiées ou interceptées. 
                            Le membre du réseau reconnaît accepter ces risques, dont l'administration de ce site ne peut en aucun cas être tenue pour responsable. 
                            Toutefois, cette même administration met de son côté en œuvre toutes les ressources techniques et humaines pour 
                            assurer un niveau permanent de sécurité conforme aux règles de l’art en vigueur dans ce domaine et pour prévenir les 
                            agressions de personnes malveillantes.
